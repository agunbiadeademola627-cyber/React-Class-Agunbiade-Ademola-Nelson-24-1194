//Declaring variables using let
let a = "Hey";

//Declaring variables using const
const PI=3.14;

//Declaring variables using var
var b=10;

//Outputs
console.log(a); //Hey
console.log(PI);//3.14
console.log(b);//10

//var and let declared variables can be updated but not const

b=15;
a="Shay";

//outputs
console.log(a);//Shay
console.log(b);//15